using System;
using System.Collections.Generic;
using System.Text;

namespace HomeWork6_NUnit_
{
    public class Calculator
    {
        public int Sum(int a, int b)
        {
            return a + b;
        }

        public int Dif(int a, int b)
        {
            return a - b;
        }

        public int Prod(int a, int b)
        {
            return a * b;
        }

        public int Div(int a, int b)
        {
            return a / b;
        }

    }
}
